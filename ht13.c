void rotate13 (uint8_t arr[]) {
    // This routine rotates the values in the lower 13 channels (channels 0-12)
    // since the flags are on channels 13 and 14, they are left alone
   uint8_t  r, g, b;
   int i, j, nrot;

   r = arr[0];
   g = arr[1];
   b = arr[2];

   nrot = 13;
   for(i = 3; i < nrot*3; i++) {
      j = i + 48;
      arr[i-3] = arr[i];
      arr[j-3] = arr[j];
     } // for i
 
   arr[nrot*3] = r;
   arr[nrot*3+1] = g;
   arr[nrot*3+2] = b;

  } // END rotate12

